const EMOJI_MAP = new Map(EMOJI_LIST.map((e) => [e.name, e]));

/** 生成内联表情 img 标签（与文字等高，行内对齐） */
export function emojiImgTag(name: string): string {
  const item = EMOJI_MAP.get(name);
  if (!item) return `[${name}]`;
  return `<img src="${item.url}" alt="${name}" class="inline-emoji" loading="lazy" />`;
}

/** 将文本中的 [表情名] 短代码替换为 img 标签 */
export function replaceEmojiShortcodes(text: string): string {
  if (!text) return text;
  return text.replace(/\[([^\]]+)\]/g, (match, name) => {
    const item = EMOJI_MAP.get(name);
    if (!item) return match;
    return emojiImgTag(name);
  });
}

/** 转义 HTML 实体，防止 XSS */
function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/**
 * 渲染纯文本评论内容：先转义 HTML 防止 XSS，再把 [表情名] 替换为 img 标签。
 * 返回安全的 HTML 字符串，可直接用于 dangerouslySetInnerHTML。
 */
export function renderTextWithEmoji(text: string): string {
  if (!text) return "";
  return replaceEmojiShortcodes(escapeHtml(text));
}

/**
 * 将短代码纯文本转为可编辑 HTML（用于 contentEditable 初始化）。
 * [表情名] → <img>，\n → <br>
 */
export function shortcodeToHtml(text: string): string {
  if (!text) return "";
  const escaped = text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
  return escaped
    .replace(/\n/g, "<br>")
    .replace(/\[([^\]]+)\]/g, (match, name) => {
      const item = EMOJI_MAP.get(name);
      if (!item) return match;
      return emojiImgTag(name);
    });
}

/**
 * 将 contentEditable 的 DOM 内容转回短代码纯文本。
 * <img alt="啊"> → [啊]，<br> / <div> → \n
 */
export function editableToShortcode(el: HTMLElement): string {
  let result = "";
  function walk(node: Node, isBlockChild = false) {
    node.childNodes.forEach((child) => {
      if (child.nodeType === Node.TEXT_NODE) {
        result += child.textContent || "";
      } else if (child.nodeType === Node.ELEMENT_NODE) {
        const e = child as HTMLElement;
        const tag = e.tagName;
        if (tag === "IMG") {
          const alt = e.getAttribute("alt");
          if (alt) result += `[${alt}]`;
        } else if (tag === "BR") {
          result += "\n";
        } else if (tag === "DIV" || tag === "P") {
          if (result && !result.endsWith("\n")) result += "\n";
          walk(e, true);
          if (!result.endsWith("\n")) result += "\n";
        } else {
          walk(e);
        }
      }
    });
  }
  walk(el);
  // 去掉末尾多余换行
  return result.replace(/\n+$/, "").trim();
}
